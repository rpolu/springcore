package com.elegent.user.model;

public class UserModel {
	private String email;
	private String userid;
	private String fname;
	private String lname;
	private String addrtess;
	private String phnum;

	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}

	/**
	 * @return the userid
	 */
	public String getUserid() {
		return userid;
	}

	/**
	 * @param userid the userid to set
	 */
	public void setUserid(String userid) {
		this.userid = userid;
	}

	/**
	 * @return the fname
	 */
	public String getFname() {
		return fname;
	}

	/**
	 * @param fname the fname to set
	 */
	public void setFname(String fname) {
		this.fname = fname;
	}

	/**
	 * @return the lname
	 */
	public String getLname() {
		return lname;
	}

	/**
	 * @param lname the lname to set
	 */
	public void setLname(String lname) {
		this.lname = lname;
	}

	/**
	 * @return the addrtess
	 */
	public String getAddrtess() {
		return addrtess;
	}

	/**
	 * @param addrtess the addrtess to set
	 */
	public void setAddrtess(String addrtess) {
		this.addrtess = addrtess;
	}

	/**
	 * @return the phnum
	 */
	public String getPhnum() {
		return phnum;
	}

	/**
	 * @param phnum the phnum to set
	 */
	public void setPhnum(String phnum) {
		this.phnum = phnum;
	}

}
